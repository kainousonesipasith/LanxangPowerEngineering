class TranslationService {
    constructor() {
        this.init();
    }

    init() {
        if (!localStorage.getItem(LANG_KEY)) {
            localStorage.setItem(LANG_KEY, "en");
        }
    }

    getLang() {
        return localStorage.getItem(LANG_KEY) || "en";
    }

    setLang(lang) {
        if (TRANSLATIONS[lang]) {
            localStorage.setItem(LANG_KEY, lang);
            if (window.utils) {
                window.utils.logAudit("LANGUAGE_CHANGED", "system", `System interface language changed to: ${lang}`);
            }
            window.location.reload();
        }
    }

    t(key) {
        if (!key) return "";
        const lang = this.getLang();
        const value = TRANSLATIONS[lang] && TRANSLATIONS[lang][key];
        
        if (value !== undefined) {
            return value;
        }

        const fallback = TRANSLATIONS["en"] && TRANSLATIONS["en"][key];
        if (fallback !== undefined) {
            return fallback;
        }

        return key;
    }
}

// Global Translation Singleton
window.i18n = new TranslationService();
window.t = (key) => window.i18n.t(key);

